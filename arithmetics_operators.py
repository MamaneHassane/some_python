# Opérateurs arithmétiques

nombre_1 = 10
nombre_2 = 3
# Addition : +
print((nombre_1 + nombre_2))
# Soustraction : -
print((nombre_1 - nombre_2))
# Multiplication : *
print((nombre_1 * nombre_2))
# Division : /
print(nombre_1/nombre_2)
# Forcer un type de résultat pour tous types d'opération
print((int)(nombre_1 / nombre_2))
# Forcer un type de résultat pour la division uniquement
print(nombre_1 // nombre_2)
# Exposant
print(nombre_1 ** nombre_2)
# Modulo
print(nombre_1 % nombre_2)