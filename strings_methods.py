# Méthodes des chaînes

snake = "Python Black"
state = "dead"
# En majuscules
print(snake.upper())
# En minuscules
print(snake.lower())
# Nombre d'occurences d'un caractères ou d'une chaîne
print(snake.lower().count('no'))
# Multiplication par un nombre
print(snake * 2) # 'Python BlackPython Black'
# Addition des chaînes
print(snake + " " + state)
# Code ASCII d'une lettre
print(ord('a'))